<script type="text/javascript">
    $(function() {
        var popupmodal = $("#staticBackdrop");

        $("#regisform").on("submit", function(e) {
            e.preventDefault(); // prevent from submitting form
            
            $.ajax({
                url: '{{ URL::to('api/register') }}',
                dataType: 'json',
                type: 'post',
                data: $(this).serialize(),
                success: function(response) {
                    // console.log(response);
                    $("#regisform")[0].reset();
                    popupmodal.find('.modal-title').text('Berhasil');
                    popupmodal.find('.modal-body').text('Registrasi berhasil, segera dialihkan..');
                    popupmodal.modal('show');

                    if (response.success) {
                        setTimeout(function() {
                            window.location.href = "{{ URL::to('/home') }}";
                        }, 3000);
                    }
                },
                error: function(response) {
                    console.log(response);
                }
            });
        });
    });
</script>

